import re


def convert_text(text: str) -> str:
    # 去掉最外层中文引号、英文引号以及已有换行
    text = text.strip().strip('“”"')
    text = text.replace("\r", "").replace("\n", "")

    # 匹配规则：
    # [*#]+     匹配一个或多个连续的 *、#
    # [^*#]*    匹配后面直到下一个 * 或 # 之前的文本
    # |[^*#]+   处理开头没有 * 或 # 的普通文本
    segments = re.findall(r"[*#]+[^*#]*|[^*#]+", text)

    return "\n".join(segment for segment in segments if segment)


